/* LED definitions **********************************************************/

/* If CONFIG_ARCH_LEDS is not defined, then the user can control the LEDs
 * in any way. The following definitions are used to access individual LEDs.
 */

/* LED index values for use with board_userled() */

#define BOARD_LED1       (GPIO_OUTPUT | GPIO_PUSHPULL | GPIO_SPEED_50MHz |\
                         GPIO_OUTPUT_CLEAR | GPIO_PORTA | GPIO_PIN5)
#define GPIO_BOARD_LED1_DIS       (GPIO_OUTPUT | GPIO_PUSHPULL | GPIO_SPEED_50MHz |\
                         GPIO_OUTPUT_CLEAR | GPIO_PORTA | GPIO_PIN4)
#define BOARD_NLEDS       1

#define BOARD_LED_RED     BOARD_LED1

/* LED bits for use with board_userled_all() */

#define BOARD_LED1_BIT    (1 << BOARD_LED1)

/* Button definitions *******************************************************/

/* The STM32F4 Discovery supports no button: */

#define BUTTON_USER        0
#define NUM_BUTTONS        1
#define BUTTON_USER_BIT    (1 << BUTTON_USER)

/* Alternate function pin selections ****************************************/

/* USART2:
 *
 */

#define GPIO_USART2_RX  GPIO_USART2_RX_1     /* PA3, P1 pin 13 */
#define GPIO_USART2_TX  GPIO_USART2_TX_1     /* PA2, P1 pin 14 */

/* PWM
 * Backlight driver
 */

#define GPIO_TIM3_CH1OUT  GPIO_TIM3_CH1OUT_1

/* SPI - There is a MPU9250 device on SPI2 using these pins: */

#define GPIO_SPI2_MISO    GPIO_SPI2_MISO_1
#define GPIO_SPI2_MOSI    GPIO_SPI2_MOSI_1
#define GPIO_SPI2_SCK     GPIO_SPI2_SCK_2
#define GPIO_SPI2_NSS     GPIO_SPI2_NSS_1

/* I2C.
 *
 * - PB10  is I2C1_SCL
 * - PB11  is I2C1_SDA
 * - PA7   is TC358870XBG RST
 */

#define GPIO_I2C2_SCL     GPIO_I2C2_SCL_1
#define GPIO_I2C2_SDA     GPIO_I2C2_SDA_1
#define GPIO_I2C2_TC358870XBG_RST     (GPIO_ALT|GPIO_AF1|GPIO_SPEED_50MHz|GPIO_PUSHPULL|GPIO_PORTA|GPIO_PIN7)
#define GPIO_I2C2_TC358870XBG_INT     (GPIO_INPUT|GPIO_FLOAT|GPIO_EXTI|GPIO_PORTB|GPIO_PIN1)

